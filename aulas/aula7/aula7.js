// para criar uma constante utilizamos "const" 
const nome = "John"; 
// não se pode criar uma constante sem inicializar ela : const nome;
// sempre se deve criar a constante e atribuir valor a ela : const nome = valor;

// não podemos modificar o valor de uma constante :
// nome = "ed";   vai  dar error pois já foi atribuido valor a ela;

// podemos alterar o valor de uma const criando outras constantes

const n1 = 3 
const n2 = 5  
const result = n1 + n2;

console.log(result); 
  

